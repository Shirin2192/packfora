    <script>
    const video = document.getElementById('bgVideo');
    const playPauseBtn = document.getElementById('playPauseBtn');
    const muteUnmuteBtn = document.getElementById('muteUnmuteBtn');

    playPauseBtn.addEventListener('click', () => {
        const icon = playPauseBtn.querySelector('i');
        if (video.paused) {
            video.play();
            icon.className = 'fas fa-pause';
        } else {
            video.pause();
            icon.className = 'fas fa-play';
        }
    });

    muteUnmuteBtn.addEventListener('click', () => {
        const icon = muteUnmuteBtn.querySelector('i');
        video.muted = !video.muted;
        icon.className = video.muted ? 'fas fa-volume-mute' : 'fas fa-volume-up';
        if (!video.muted) {
            video.volume = 1; // reset volume when unmuted manually
        }
    });

    // Scroll event listener to adjust volume
    window.addEventListener('scroll', () => {
        const scrollTop = window.scrollY || document.documentElement.scrollTop;
        const docHeight = document.documentElement.scrollHeight - window.innerHeight;
        const scrollPercent = docHeight > 0 ? scrollTop / docHeight : 0;

        // Calculate volume: full volume at top, 0 at bottom
        let newVolume = 1 - scrollPercent;
        if (newVolume < 0) newVolume = 0;
        if (newVolume > 1) newVolume = 1;

        // Update video volume only if not muted manually
        if (!video.muted) {
            video.volume = newVolume;
        }

        // If volume is 0, mute video and update icon, else unmute and update icon
        const icon = muteUnmuteBtn.querySelector('i');
        if (newVolume === 0) {
            video.muted = true;
            icon.className = 'fas fa-volume-mute';
        } else if (!video.muted) {
            icon.className = 'fas fa-volume-up';
        }
    });
    </script>
